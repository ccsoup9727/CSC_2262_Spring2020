function f = lab5bf(u, uf)
    m1 = .9;
    m2 = .5;
    m3 = .6;
    m4 = .2;
    m5 = .8;
    m6 = .7;
    m7 = .3;
    k1 = 3.5;
    k2 = 5.4;
    k3 = 4.4;
    k4 =  4.9;
    k5 =  4.7;
    k6 =  5.3;
    k7 = 3.9;
    k8 = 5.2;
    
    c1 = .20;
    c2 = .12;
    c3 = .24;
    c4 = .32;
    c5 = .14;
    c6 = .28;
    c7 = .36;
    c8 = .22;
    
    x1 = uf(1);
    v1 = uf(2);
    
    x2 = uf(3);
    v2 = uf(4);
    
    x3= uf(5);
    v3 = uf(6);
    
    x4= uf(7);
    v4 = uf(8);
    
        
    x5= uf(9);
    v5 = uf(10);
    
        
    x6= uf(11);
    v6 = uf(12);
    
        
    x7= uf(13);
    v7 = uf(14);
    
        
 
    f = zeros(14,1);
    f(1) = v1;
    f(2) = (1/m1) * (-k1*x1 + k2*(x2-x1) - c1*v1 + c2 * (v2-v1));
    
    f(3) = v2;
    f(4) = (1/m2) * (-k2*(x2-x1) + k3*(x3-x2) - c2*(v2-v1) + c3*(v3-v2));
    
    f(5) = v3;
    f(6) = (1/m3) * (-k3*(x3-x2) + k4*(x4-x3) - c3*(v3-v2) + c4*(v4-v3));
    
    f(7) = v4;
    f(8) = (1/m4) * (-k4*(x4-x3) + k5*(x5-x4) - c4*(v4-v3) + c5*(v5-v4));
    
    f(9) = v5;
    f(10) = (1/m5) * (-k5*(x5-x4) + k6*(x6-x5) - c5*(v5-v4) + c6*(v6-v5));
    
    f(11) = v6;
    f(12) = (1/m6) * (-k6*(x6-x5) + k7*(x7-x6) - c6*(v6-v5) + c7*(v7-v6));
    
    f(13) = v7;
    f(14) = (1/m7) * (-k7*(x7-x6)-k8*x7 - c7*(v7-v6) - c8*v7);
    
end